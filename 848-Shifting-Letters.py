# Условие задачи (на русском):
# Нам дана строка 's' и массив целых чисел 'shifts'.
# Сдвиг буквы означает замену её на следующую в алфавите (z переходит в a).
#
# Для каждого индекса 'i' в массиве 'shifts', нам нужно сдвинуть ПЕРВЫЕ 'i + 1'
# символов строки 's' на указанное количество раз.
#
# Пример 1: s = "abc", shifts = [3, 5, 9]
# 1. Сдвигаем 1-ю букву на 3: "dbc"
# 2. Сдвигаем первые 2 буквы на 5: "igc" (d+5=i, b+5=g)
# 3. Сдвигаем первые 3 буквы на 9: "rpl" (i+9=r, g+9=p, c+9=l)
# Результат: "rpl"
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Суффиксные суммы + Остаток от деления
#
# Хитрость: Заметим, что:
# - Первая буква сдвигается всеми числами из 'shifts' (3 + 5 + 9 = 17 раз).
# - Вторая буква сдвигается всеми, начиная со второго (5 + 9 = 14 раз).
# - Третья — только последним (9 раз).
#
# Вместо того чтобы мучить строку много раз, мы можем посчитать ИТОГОВЫЙ сдвиг
# для каждой буквы, двигаясь С КОНЦА массива. Это называется суффиксная сумма.
#
# 1. Подготовка сдвигов:
#    Идем с конца 'shifts' и накапливаем сумму.
#    shifts = [3, 5, 9] -> превращается в [17, 14, 9].
#    Важно: берем сумму по модулю 26 (количество букв), чтобы числа не были гигантскими.
#
# 2. Применение сдвигов:
#    Проходим по строке один раз и сдвигаем каждый символ на его итоговое число.
#
# Пример 1 детально:
# s = "abc", shifts = [3, 5, 9]
# - Суффиксные суммы:
#   Последний: 9
#   Предпоследний: 5 + 9 = 14
#   Первый: 3 + 14 = 17
# - Итоговые сдвиги: [17, 14, 9]
# - 'a' + 17 = 'r'
# - 'b' + 14 = 'p'
# - 'c' + 9  = 'l'
# Результат: "rpl"

class Solution(object):
    def shiftingLetters(self, s, shifts):
        # 1. Считаем суффиксные суммы сдвигов с конца
        total_shift = 0
        # Превращаем s в список, так как строки в Python неизменяемы
        ans = list(s)

        for i in range(len(shifts) - 1, -1, -1):
            # Накапливаем сдвиг и держим его в пределах 0-25
            total_shift = (total_shift + shifts[i]) % 26

            # 2. Сдвигаем текущий символ
            # ord(c) дает код символа, chr(n) возвращает символ по коду
            # Вычитаем ord('a'), чтобы работать в диапазоне 0-25
            start_pos = ord(ans[i]) - ord('a')
            new_pos = (start_pos + total_shift) % 26
            ans[i] = chr(new_pos + ord('a'))

        return "".join(ans)

if __name__ == '__main__':
    solution = Solution()

    s = "abc"
    shifts = [3, 5, 9]
    result = solution.shiftingLetters(s, shifts)
    print(result)
    assert result == "rpl"

    s = "aaa"
    shifts = [1, 2, 3]
    result = solution.shiftingLetters(s, shifts)
    print(result)
    assert result == "gfd"
