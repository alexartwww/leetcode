# leetcode: https://leetcode.com/problems/longest-repeating-character-replacement/

# Условие задачи (на русском):
# Дан массив ПОЛОЖИТЕЛЬНЫХ чисел и число 'target'.
# Нужно найти минимальную длину подмассива, сумма элементов которого >= target.
# Если такого подмассива нет, вернуть 0.
#
# Пример: target = 7, nums = [2, 3, 1, 2, 4, 3]
# Подмассив [4, 3] имеет длину 2 и сумму 7. Это минимальная длина.
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Скользящее окно (два указателя)
# Алгоритм решения: Скользящее окно + Хеш-таблица (Frequency Map)
#
# Идея:
# Мы расширяем правую границу окна, пока сумма меньше target.
# Как только сумма стала >= target, мы начинаем максимально сдвигать
# левую границу вправо, чтобы уменьшить длину окна, пока условие выполняется.
#
# 1. Инициализация:
#    - left = 0 (левая граница).
#    - current_sum = 0 (сумма в текущем окне).
#    - min_length = бесконечность (для поиска минимума).
#
# 2. Цикл по правой границе (right):
#    - Добавляем nums[right] к current_sum.
#    - Пока current_sum >= target:
#        - Обновляем min_length: min(min_length, right - left + 1).
#        - Вычитаем nums[left] из суммы и двигаем left вперед (сжимаем окно).
#
# 3. Итог: Если min_length не изменился — возвращаем 0, иначе возвращаем min_length.
#
# Суть: Мы ищем самое длинное окно [left, right], в котором можно заменить
# не более k символов так, чтобы все символы в окне стали одинаковыми.
#
# Главная формула:
# Количество замен = (Длина окна) - (Количество самого частого символа в окне)
# Если (right - left + 1) - max_freq <= k, то окно "валидное".
#
# 1. Инициализация:
#    - count = {} (словарь для частоты символов в текущем окне)
#    - left = 0 (левая граница)
#    - max_freq = 0 (частота самого популярного символа в окне)
#    - max_len = 0 (результат)
#
# 2. Расширение (цикл по right):
#    - Добавляем символ s[right] в словарь частот.
#    - Обновляем max_freq: это максимум между старым max_freq и новым символом.
#
# 3. Проверка и сжатие (if):
#    - Если количество символов, которые НУЖНО заменить (длина - max_freq), больше k:
#      - Окно "испорчено". Нам нужно сдвинуть левую границу.
#      - Уменьшаем частоту s[left] в словаре и делаем left += 1.
#
# 4. Результат:
#    - На каждом шаге обновляем max_len = max(max_len, right - left + 1).
#
# Пример: s = "AABABBA", k = 1
#
# - Окно [AABA]: длина 4, самый частый 'A' (3 раза).
#   Замен нужно: 4 - 3 = 1. У нас k=1, подходит! max_len = 4.
# - Окно [AABAB]: длина 5, частые 'A'(3) или 'B'(2).
#   Замен нужно: 5 - 3 = 2. Это > k. Сдвигаем left.
# - Новое окно [ABAB]: длина 4... и так далее.

class Solution(object):
    def characterReplacement(self, s, k):
        count = {}
        max_len = 0
        max_freq = 0
        left = 0

        for right in range(len(s)):
            # Увеличиваем счетчик текущего символа
            count[s[right]] = 1 + count.get(s[right], 0)
            # Обновляем максимальную частоту в текущем окне
            max_freq = max(max_freq, count[s[right]])

            # Если количество "чужих" символов больше k
            while (right - left + 1) - max_freq > k:
                count[s[left]] -= 1
                left += 1
                # Примечание: max_freq можно не пересчитывать при сдвиге left.
                # Даже если частота упала, max_len обновится только когда мы найдем
                # новую, еще большую max_freq.

            max_len = max(max_len, right - left + 1)

        return max_len


if __name__ == '__main__':
    solution = Solution()

    print(solution.characterReplacement("ABAB", 2), 4)
    print(solution.characterReplacement("AABABBA", 1), 4)
    print(solution.characterReplacement("ABBB", 1), 4)
