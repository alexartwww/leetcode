# leetcode: https://leetcode.com/problems/longest-substring-without-repeating-characters/

# Эта задача — классика на метод Sliding Window (Скользящее окно), но уже с динамическим размером. В отличие
# от предыдущей задачи, где окно было фиксированной длины $k$, здесь окно будет расширяться и сужаться
# в зависимости от того, встречали ли мы этот символ раньше.
#
# Условие задачи (на русском):
# Дана строка 's'. Нужно найти длину самой длинной подстроки
# без повторяющихся символов.
#
# Пример 1: s = "abcabcbb" -> Ответ: 3 ("abc")
# Пример 3: s = "pwwkew" -> Ответ: 3 ("wke")
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Скользящее окно + Хеш-таблица (Dictionary)
#
# Идея:
# Мы используем два указателя (левый 'l' и правый 'r'), чтобы создать "окно".
# Также мы заводим словарь, чтобы помнить, на каком индексе мы в последний раз
# видели каждый символ.
#
# 1. Правый указатель 'r' постоянно двигается вперед, расширяя окно.
# 2. Если мы встречаем символ, который уже есть в словаре:
#    - И его последний индекс находится ВНУТРИ нашего текущего окна,
#    - То мы резко "прыгаем" левым указателем 'l' за этот индекс.
# 3. На каждом шаге считаем длину окна (r - l + 1) и обновляем максимум.
#
# -----------------------------------------------------------------------------

class Solution:
    def lengthOfLongestSubstring(self, s):
        char_map = {}  # Символ -> Его последний индекс
        max_length = 0
        l = 0  # Левая граница окна

        for r in range(len(s)):
            char = s[r]

            # Если символ уже встречался и он в текущем окне
            if char in char_map and char_map[char] >= l:
                # Сдвигаем левую границу сразу за старое местоположение этого символа
                l = char_map[char] + 1

            # Обновляем/записываем индекс символа
            char_map[char] = r

            # Считаем текущую длину окна и обновляем рекорд
            max_length = max(max_length, r - l + 1)

        return max_length


if __name__ == '__main__':
    solution = Solution()

    s = "abcabcbb"
    result = solution.lengthOfLongestSubstring(s)
    print(result)
    assert result == 3

    s = "bbbbb"
    result = solution.lengthOfLongestSubstring(s)
    print(result)
    assert result == 1

    s = "pwwkew"
    result = solution.lengthOfLongestSubstring(s)
    print(result)
    assert result == 3
