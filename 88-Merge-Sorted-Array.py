# leetcode: https://leetcode.com/problems/merge-sorted-array/

# Эта задача — классика на метод Two Pointers (Два указателя), но с одной важной деталью: чтобы не перезаписывать
# элементы в nums1, которые нам еще нужны, мы будем заполнять массив с конца в начало.

# Поскольку в конце nums1 уже зарезервировано место (нули), самые большие числа встанут туда первыми,
# не мешая остальным.

# Условие задачи (на русском):
# Даны два отсортированных массива nums1 и nums2.
# Нужно объединить их в один отсортированный массив ПРЯМО ВНУТРИ nums1.
# В nums1 уже есть место в конце (заполнено нулями), чтобы вместить nums2.
# m — количество реальных элементов в nums1, n — в nums2.
#
# Пример 1: nums1 = [1,2,3,0,0,0], m = 3, nums2 = [2,5,6], n = 3
# Результат: nums1 станет [1,2,2,3,5,6]
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Три указателя (с конца в начало)
#
# Идея: Сравниваем самые большие элементы обоих массивов и ставим
# самый крупный в самый конец nums1. Так мы ничего не затрем.
#
# 1. Указатели:
#    - p1 = m - 1 (конец реальных данных в nums1)
#    - p2 = n - 1 (конец массива nums2)
#    - p = m + n - 1 (самый последний индекс в nums1, куда пишем результат)
#
# 2. Цикл: Пока есть элементы в nums2 (p2 >= 0):
#    - Если в nums1 еще есть элементы и nums1[p1] > nums2[p2]:
#        Ставим nums1[p1] в позицию p, сдвигаем p1 и p.
#    - Иначе:
#        Ставим nums2[p2] в позицию p, сдвигаем p2 и p.
#
# 3. Краевой случай:
#    Если p1 закончился раньше, чем p2, мы просто копируем остатки nums2.
#    Если p2 закончился раньше — всё готово, так как остатки nums1 уже на местах.
# -----
# Условие задачи (на русском):
# Даны два отсортированных массива nums1 и nums2.
# Нужно объединить их в один отсортированный массив ПРЯМО ВНУТРИ nums1.
# В nums1 зарезервировано место в конце (заполнено нулями) для элементов nums2.
# m — количество реальных элементов в nums1, n — в nums2.
#
# Пример: nums1 = [1,2,3,0,0,0], m = 3, nums2 = [2,5,6], n = 3
# Результат: nums1 станет [1,2,2,3,5,6]
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Три указателя (с конца в начало)
#
# Идея: Чтобы не затирать нужные данные в nums1 и не использовать доп. память,
# мы сравниваем самые большие элементы из обоих массивов и ставим
# "победителя" в самый конец nums1 (индекс m + n - 1).
#
# 1. Ставим указатели:
#    - p1 на последний "реальный" элемент nums1 (m - 1).
#    - p2 на последний элемент nums2 (n - 1).
#    - p на самую последнюю ячейку всего массива nums1 (m + n - 1).
#
# 2. Пока в nums2 остались элементы (p2 >= 0):
#    - Сравниваем nums1[p1] и nums2[p2].
#    - Большее число пишем в nums1[p] и сдвигаем соответствующий указатель.
#
# 3. Итог:nums1 полностью заполнен и отсортирован.
#    Дополнительная память O(1), время O(m + n).

class Solution(object):
    def merge(self, nums1, m, nums2, n):
        # Указатель для nums1 (реальные данные)
        p1 = m - 1
        # Указатель для nums2
        p2 = n - 1
        # Указатель для записи результата (самый конец nums1)
        p = m + n - 1

        # Идем, пока не закончатся элементы во ВТОРОМ массиве
        while p2 >= 0:
            # Если в первом массиве еще есть элементы и текущий больше, чем во втором
            if p1 >= 0 and nums1[p1] > nums2[p2]:
                nums1[p] = nums1[p1]
                p1 -= 1
            else:
                # Если во втором число больше или в первом элементы кончились
                nums1[p] = nums2[p2]
                p2 -= 1

            # В любом случае сдвигаем указатель записи
            p -= 1

        # Если в nums2 еще остались элементы, переносим их
        # (Если остались в nums1 - они и так уже на местах)
        while p2 >= 0:
            nums1[p] = nums2[p2]
            p2 -= 1
            p -= 1

