# Эта задача — отличный пример имитационного моделирования (Simulation).
# Здесь не нужно придумывать хитрые алгоритмы, достаточно просто «прожить»
# день вместе с поваром, аккуратно считая время.
#
# Условие задачи (на русском):
# В ресторане один повар. Дан массив 'customers', где customers[i] = [приход, время_готовки].
# Повар готовит блюда строго в порядке очереди.
# Если повар свободен, он начинает готовить сразу. Если занят — клиент ждет.
# Время ожидания клиента = (время окончания готовки) - (время прихода клиента).
# Нужно вернуть СРЕДНЕЕ время ожидания всех клиентов.
#
# Пример 1: customers = [[1,2],[2,5],[4,3]]
# 1. Пришел в 1, готовка 2. Закончил в 3. Ждал: 3 - 1 = 2.
# 2. Пришел в 2, но повар занят до 3. Закончил в 3 + 5 = 8. Ждал: 8 - 2 = 6.
# 3. Пришел в 4, но повар занят до 8. Закончил в 8 + 3 = 11. Ждал: 11 - 4 = 7.
# Среднее: (2 + 6 + 7) / 3 = 5.0
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Моделирование процесса (Simulation)
#
# Идея: Нам нужно поддерживать переменную 'chef_free_time' — момент времени,
# когда повар освободится от текущего заказа.
#
# 1. Инициализация:
#    - total_waiting_time = 0 (сумма всех ожиданий).
#    - chef_free_time = 0 (момент, когда повар закончит текущий заказ).
#
# 2. Проход по каждому клиенту [arrival, time]:
#    - Когда повар НАЧНЕТ готовить?
#      Либо в момент прихода клиента (arrival), либо когда освободится (chef_free_time).
#      Берем максимум: start_time = max(arrival, chef_free_time).
#    - Когда повар ЗАКОНЧИТ?
#      finish_time = start_time + time.
#    - Сколько ждал этот клиент?
#      wait = finish_time - arrival.
#    - Обновляем переменные:
#      total_waiting_time += wait
#      chef_free_time = finish_time
#
# 3. Результат:
#    - Делим total_waiting_time на количество клиентов.

class Solution(object):
    def averageWaitingTime(self, customers):
        total_waiting_time = 0
        chef_free_time = 0

        for arrival, time in customers:
            # Повар начнет готовить либо сразу по приходу,
            # либо как только освободится от предыдущего заказа
            start_time = max(arrival, chef_free_time)

            # Время окончания готовки для текущего клиента
            finish_time = start_time + time

            # Считаем, сколько клиент провел в ресторане (ожидание + готовка)
            total_waiting_time += (finish_time - arrival)

            # Обновляем время, когда повар снова будет свободен
            chef_free_time = finish_time

        return float(total_waiting_time) / len(customers)


if __name__ == '__main__':
    solution = Solution()

    customers = [[1, 2], [2, 5], [4, 3]]
    result = solution.averageWaitingTime(customers)
    print(result)
    assert result == 5.00000

    customers = [[5, 2], [5, 4], [10, 3], [20, 1]]
    result = solution.averageWaitingTime(customers)
    print(result)
    assert result == 3.25000
