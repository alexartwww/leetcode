# leetcode: https://leetcode.com/problems/minimum-size-subarray-sum/

# Эта задача — классический сценарий для использования техники Sliding Window (Скользящее окно) с динамическим размером.
# В отличие от задач, где окно фиксировано, здесь мы будем «сжимать» и «растягивать» его, чтобы найти
# минимально возможную длину.

# Условие задачи (на русском):
# Дан массив ПОЛОЖИТЕЛЬНЫХ чисел и число 'target'.
# Нужно найти минимальную длину подмассива, сумма элементов которого >= target.
# Если такого подмассива нет, вернуть 0.
#
# Пример: target = 7, nums = [2, 3, 1, 2, 4, 3]
# Подмассив [4, 3] имеет длину 2 и сумму 7. Это минимальная длина.
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Скользящее окно (два указателя)
#
# Идея:
# Мы расширяем правую границу окна, пока сумма меньше target.
# Как только сумма стала >= target, мы начинаем максимально сдвигать
# левую границу вправо, чтобы уменьшить длину окна, пока условие выполняется.
#
# 1. Инициализация:
#    - left = 0 (левая граница).
#    - current_sum = 0 (сумма в текущем окне).
#    - min_length = бесконечность (для поиска минимума).
#
# 2. Цикл по правой границе (right):
#    - Добавляем nums[right] к current_sum.
#    - Пока current_sum >= target:
#        - Обновляем min_length: min(min_length, right - left + 1).
#        - Вычитаем nums[left] из суммы и двигаем left вперед (сжимаем окно).
#
# 3. Итог: Если min_length не изменился — возвращаем 0, иначе возвращаем min_length.

# Алгоритм решения: Динамическое скользящее окно (Sliding Window)
#
# Суть: Мы создаем окно, которое расширяется вправо, пока сумма элементов внутри
# не станет >= target. Как только цель достигнута, мы начинаем сужать окно слева,
# чтобы найти минимально возможную длину, при которой условие все еще выполняется.
#
# 1. Инициализация:
#    - left = 0 (левая граница окна)
#    - current_sum = 0 (текущая сумма в окне)
#    - min_length = бесконечность (сюда будем записывать самый короткий результат)
#
# 2. Расширение (цикл по right):
#    - Идем правым указателем по массиву и добавляем nums[right] к current_sum.
#
# 3. Сжатие (внутренний цикл while):
#    - Пока current_sum >= target:
#      - Считаем длину текущего окна: right - left + 1.
#      - Обновляем min_length, если нашли вариант короче.
#      - Вычитаем nums[left] из суммы и сдвигаем левую границу (left += 1).
#      - Это позволяет нам "выбросить" лишние элементы в начале, которые не нужны для суммы.
#
# Пример: target = 7, nums = [2, 3, 1, 2, 4, 3]
#
# - Окно растет: [2], [2, 3], [2, 3, 1], [2, 3, 1, 2]. Сумма 8 (>= 7).
# - Длина 4. Пытаемся сжать слева:
# - Убираем 2: [3, 1, 2]. Сумма 6 (< 7). Остановка.
# - Окно растет дальше: [3, 1, 2, 4]. Сумма 10.
# - Сжимаем слева: убираем 3 -> [1, 2, 4] (сумма 7, длина 3).
# - Сжимаем еще: убираем 1 -> [2, 4] (сумма 6, мало).
# - Окно растет дальше: [2, 4, 3]. Сумма 9.
# - Сжимаем слева: убираем 2 -> [4, 3] (сумма 7, длина 2!). Это наш минимум.

class Solution(object):
    def minSubArrayLen(self, target, nums):
        left = 0
        current_sum = 0
        # Используем float('inf') для обозначения бесконечности
        min_length = float('inf')

        for right in range(len(nums)):
            current_sum += nums[right]

            # Как только набрали нужную сумму, пытаемся максимально сузить окно
            while current_sum >= target:
                min_length = min(min_length, right - left + 1)
                current_sum -= nums[left]
                left += 1

        # Если min_length не изменился, значит подходящего подмассива не нашли
        return min_length if min_length != float('inf') else 0

if __name__ == '__main__':
    solution = Solution()

    target = 7
    nums = [2, 3, 1, 2, 4, 3]
    result = solution.minSubArrayLen(target, nums)
    print(result)
    assert result == 2

    target = 4
    nums = [1, 4, 4]
    result = solution.minSubArrayLen(target, nums)
    print(result)
    assert result == 1

    target = 11
    nums = [1, 1, 1, 1, 1, 1, 1, 1]
    result = solution.minSubArrayLen(target, nums)
    print(result)
    assert result == 0
