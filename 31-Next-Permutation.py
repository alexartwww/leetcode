# leetcode: https://leetcode.com/problems/next-permutation/

# Эта задача — одна из самых красивых в плане логики. Она кажется сложной, пока не увидишь закономерность в том,
# как меняются числа при лексикографическом упорядочивании.

# Основная идея: чтобы получить следующее число, нам нужно изменить массив как можно "правее" и как можно "меньше".

# Условие задачи (на русском):
# Найти следующую лексикографическую перестановку чисел.
# Если массив отсортирован по убыванию (самая большая перестановка),
# нужно вернуть самый минимальный вариант (отсортированный по возрастанию).
# Важно: делать всё in-place (на месте), не используя лишнюю память.
#
# Пример: [1, 2, 3] -> [1, 3, 2]
# Пример: [3, 2, 1] -> [1, 2, 3]
#
# -----------------------------------------------------------------------------
# Алгоритм «Три шага»
#
# Представим число как график. Если числа идут по возрастанию, мы можем легко сделать его больше.
# Если по убыванию — это локальный пик, и там переставлять уже нечего.
#
# 1. Найдите «точку падения» (Pivot): Идите с конца массива и найдите первый элемент, который меньше
# соседа справа (nums[i] < nums[i+1]). Это место, где возрастающая (справа налево) последовательность ломается.
#
#  - Если такого элемента нет (весь массив убывает), значит, это последняя перестановка — просто разверните весь массив.
#
# 2. Найдите «наименьшее из больших»: Снова идите с конца и найдите первое число, которое строго больше вашего Pivot.
#    Поменяйте их местами.
#
# 3. Разверните хвост: Все числа справа от позиции Pivot теперь гарантированно идут по убыванию. Чтобы сделать
#    число минимально возможным, просто разверните эту часть массива (сделайте её возрастающей).
#
# Пример: nums = [1, 5, 8, 4, 7, 6, 5, 3, 1]
# Ищем Pivot: Идем с конца: 1, 3, 5, 6, 7... Опа - 4! 4 меньше чем 7. Наш Pivot — это 4 (индекс 3).
#
# Ищем замену: Какое число правее 4 является самым маленьким, но все еще больше 4? Идем с конца: 1, 3... 5.
#
# Меняем 4 и 5 местами: [1, 5, 8, **5**, 7, 6, **4**, 3, 1]
#
# Разворачиваем: Хвост после индекса 3 (это [7, 6, 4, 3, 1]) разворачиваем в [1, 3, 4, 6, 7].
#
# Итог: [1, 5, 8, 5, 1, 3, 4, 6, 7]

class Solution(object):
    def nextPermutation(self, nums):
        i = len(nums) - 2
        # 1. Находим pivot
        while i >= 0 and nums[i] >= nums[i + 1]:
            i -= 1

        if i >= 0:
            # 2. Находим число для обмена
            j = len(nums) - 1
            while nums[j] <= nums[i]:
                j -= 1
            nums[i], nums[j] = nums[j], nums[i]

        # 3. Разворачиваем хвост (делаем его минимально возможным)
        left, right = i + 1, len(nums) - 1
        while left < right:
            nums[left], nums[right] = nums[right], nums[left]
            left += 1
            right -= 1
        return nums


if __name__ == '__main__':
    solution = Solution()

    nums = [1, 2, 3]
    result = solution.nextPermutation(nums)
    print(result)
    assert result == [1, 3, 2]

    nums = [3, 2, 1]
    result = solution.nextPermutation(nums)
    print(result)
    assert result == [1, 2, 3]

    nums = [1, 1, 5]
    result = solution.nextPermutation(nums)
    print(result)
    assert result == [1, 5, 1]

