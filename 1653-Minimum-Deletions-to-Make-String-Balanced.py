# Условие задачи (на русском):
# Дана строка 's', состоящая только из букв 'a' и 'b'.
# Строка считается сбалансированной, если нет такой ситуации, что 'b' стоит
# левее, чем 'a' (т.е. сначала должны идти все 'a', потом все 'b').
# Нужно найти МИНИМАЛЬНОЕ количество удалений символов, чтобы строка стала сбалансированной.
#
# Пример 1: s = "aababbab"
# Если удалить 'b' на индексе 2 и 'a' на индексе 6, получится "aaabbb" — это ок.
# Результат: 2
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Динамическое программирование (или жадный подход со стеком)
#
# Идея: Мы идем по строке слева направо и следим за количеством встреченных 'b'.
# Как только мы встречаем 'a', у нас есть два выбора:
# 1. Удалить эту 'a' (чтобы она не стояла после уже встреченных 'b').
# 2. Оставить эту 'a', но тогда придется удалить одну из ранее встреченных 'b'.
#
# Мы всегда выбираем минимум из этих двух вариантов.
#
# 1. Инициализация:
#    - deletions = 0 (счетчик необходимых удалений).
#    - b_count = 0 (сколько символов 'b' мы встретили на данный момент).
#
# 2. Проход по строке:
#    - Если встречаем 'b': просто увеличиваем b_count. Это потенциальные кандидаты на удаление.
#    - Если встречаем 'a':
#      - У нас конфликт (есть 'b' слева от 'a').
#      - Либо мы удаляем текущую 'a' (deletions + 1),
#      - Либо мы "удаляем" одну из накопленных 'b' (чтобы сбалансировать эту 'a').
#      - Обновляем deletions = min(deletions + 1, b_count).
#
# Пример 1 детально: s = "aababbab"
# 1. 'a': b=0, del=0
# 2. 'a': b=0, del=0
# 3. 'b': b=1, del=0
# 4. 'a': Нашли 'a' после 'b'! del = min(0+1, 1) = 1. (Удаляем либо эту 'a', либо ту 'b')
# 5. 'b': b=2, del=1
# 6. 'b': b=3, del=1
# 7. 'a': Нашли 'a'! del = min(1+1, 3) = 2.
# 8. 'b': b=4, del=2
# Итог: 2

class Solution(object):
    def minimumDeletions(self, s):
        deletions = 0
        b_count = 0

        for char in s:
            if char == 'a':
                # Если мы встретили 'a' и до неё были 'b',
                # мы выбираем: либо удалить текущую 'a', либо одну из прошлых 'b'
                deletions = min(deletions + 1, b_count)
            else:
                # Если встретили 'b', просто запоминаем её
                b_count += 1

        return deletions


if __name__ == '__main__':
    solution = Solution()

    s = "aababbab"
    result = solution.minimumDeletions(s)
    print(result)
    assert result == 2

    s = "bbaaaaabb"
    result = solution.minimumDeletions(s)
    print(result)
    assert result == 2
