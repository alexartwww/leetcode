# leetcode: https://leetcode.com/problems/string-compression/

# Дан массив символов chars. Сожмите его, используя следующий алгоритм:
#
# Начните с пустой строки s. Для каждой группы последовательных повторяющихся символов в chars:
#
# Если длина группы равна 1, добавьте символ в s.
#
# В противном случае добавьте символ, за которым следует длина группы.
# Сжатая строка s не должна возвращаться отдельно, а должна храниться во входном массиве символов chars.
# Обратите внимание, что группы длиной 10 или более символов будут разбиты на несколько символов в chars.
#
# После завершения изменения входного массива верните новую длину массива.
#
# Необходимо написать алгоритм, использующий только постоянное дополнительное пространство.
#
# Примечание: символы в массиве, выходящие за пределы возвращаемой длины, не имеют значения и должны игнорироваться.

# Условие задачи (на русском):
# Дан массив символов 'chars'. Нужно сжать его по правилу:
# 1. Если символ повторяется 1 раз, пишем просто символ.
# 2. Если символ повторяется несколько раз (k), пишем символ и число k.
# 3. Числа больше 9 разбиваются на отдельные символы: 12 -> '1', '2'.
# Важно: Изменения должны быть внутри исходного массива (in-place).
# Нужно вернуть новую длину массива.
#
# Пример: ["a","a","b","b","c","c","c"] -> ["a","2","b","2","c","3"], вернуть 6.
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Два указателя (чтение и запись)
#
# Идея:
# Мы используем указатель 'write', чтобы знать, куда записывать результат,
# и указатель 'read', чтобы сканировать исходные данные.
#
# 1. Запускаем цикл по массиву (указатель 'read').
# 2. Находим группу одинаковых символов и считаем их количество (count).
# 3. Записываем текущий символ в позицию 'write' и двигаем 'write' на 1.
# 4. Если count > 1:
#    Превращаем count в строку и записываем каждую цифру по очереди,
#    каждый раз сдвигая 'write'.
# 5. Возвращаем 'write' — это и будет новая длина.
#
# -----------------------------------------------------------------------------


class Solution(object):
    def compress(self, chars):
        if len(chars) == 0:
            return 0
        current_char = ""
        p_char = 0
        p_last = 0
        counter = 0

        for p, c in enumerate(chars):
            if current_char == "":
                current_char = chars[p]
                p_char = p
                p_last = p
                counter = 0
            if c != current_char:
                chars[p_char] = current_char
                p_last = p_char
                if counter > 1:
                    for p2, c2 in enumerate(list(str(counter))):
                        chars[p_char+1+p2] = c2
                        p_last = p_char+1+p2
                        p_char += 1
                p_char += 1
                current_char = c
                counter = 1
            else:
                counter += 1

        chars[p_char] = current_char
        p_last = p_char
        if counter > 1:
            for p2, c2 in enumerate(list(str(counter))):
                chars[p_char + 1 + p2] = c2
                p_last = p_char + 1 + p2

        chars = chars[:p_last+1]
        return chars


class Solution2(object):
    def compress(self, chars):
        write = 0
        read = 0
        n = len(chars)

        while read < n:
            char = chars[read]
            count = 0

            # Считаем количество повторяющихся символов
            while read < n and chars[read] == char:
                read += 1
                count += 1

            # Записываем сам символ
            chars[write] = char
            write += 1

            # Если символов больше одного, записываем число
            if count > 1:
                for digit in str(count):
                    chars[write] = digit
                    write += 1

        return write


