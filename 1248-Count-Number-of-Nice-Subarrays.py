# Условие задачи (на русском):
# Дан массив целых чисел 'nums' и число 'k'. Подмассив называется "хорошим",
# если в нем ровно 'k' нечетных чисел.
# Нужно вернуть ОБЩЕЕ КОЛИЧЕСТВО таких "хороших" подмассивов.
#
# Пример 1: nums = [1, 1, 2, 1, 1], k = 3
# - Подмассив [1, 1, 2, 1] содержит три нечетных числа.
# - Подмассив [1, 2, 1, 1] содержит три нечетных числа.
# Итог: 2
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Префиксные суммы (Prefix Sums) + Хеш-таблица
#
# Идея: Превратим задачу в поиск суммы подмассива.
# 1. Заменим каждое НЕЧЕТНОЕ число на 1, а каждое ЧЕТНОЕ на 0.
# 2. Теперь задача звучит так: "Найдите количество подмассивов, сумма которых равна k".
#
# Математика:
# Если текущая сумма нечетных чисел от начала до индекса 'j' равна S,
# а на каком-то предыдущем индексе 'i' сумма была равна (S - k),
# то на отрезке между 'i' и 'j' находится ровно 'k' нечетных чисел.
#
# 1. Инициализация:
#    - count_map = {0: 1}. Мы уже встретили сумму 0 один раз (в самом начале).
#    - current_odds = 0 (текущее количество нечетных чисел).
#    - result = 0 (счетчик найденных подмассивов).
#
# 2. Проход по массиву:
#    - Если число нечетное, current_odds += 1.
#    - Проверяем, встречалась ли нам раньше сумма (current_odds - k).
#    - Если да, прибавляем к результату количество раз, которое она встречалась.
#    - Сохраняем/обновляем текущую сумму в словаре.
#
# Пример 1 детально: nums = [1, 1, 2, 1, 1], k = 3
# - Старт: map = {0: 1}, res = 0
# - [1]: odds=1. Ищем (1-3)=-2. Нет. map = {0:1, 1:1}
# - [1, 1]: odds=2. Ищем (2-3)=-1. Нет. map = {0:1, 1:1, 2:1}
# - [1, 1, 2]: odds=2. Ищем (2-3)=-1. Нет. map = {0:1, 1:1, 2:2} (четное число не меняет odds)
# - [1, 1, 2, 1]: odds=3. Ищем (3-3)=0. 0 встречался 1 раз. res = 1. map = {0:1, 1:1, 2:2, 3:1}
# - [1, 1, 2, 1, 1]: odds=4. Ищем (4-3)=1. 1 встречался 1 раз. res = 1 + 1 = 2.
# Итог: 2

class Solution(object):
    def numberOfSubarrays(self, nums, k):
        # Словарь: {префиксная_сумма_нечетных: сколько_раз_встретилась}
        count_map = {0: 1}
        current_odds = 0
        result = 0

        for n in nums:
            # Увеличиваем счетчик, если число нечетное (остаток от деления на 2)
            current_odds += n % 2

            # Если (текущие - k) уже встречалось, значит между теми точками
            # и текущей ровно k нечетных чисел
            if (current_odds - k) in count_map:
                result += count_map[current_odds - k]

            # Записываем текущую сумму в словарь
            count_map[current_odds] = count_map.get(current_odds, 0) + 1

        return result


if __name__ == '__main__':
    solution = Solution()

    nums = [1, 1, 2, 1, 1]
    k = 3
    result = solution.numberOfSubarrays(nums, k)
    print(result)
    assert result == 2

    nums = [2, 4, 6]
    k = 1
    result = solution.numberOfSubarrays(nums, k)
    print(result)
    assert result == 0

    nums = [2, 2, 2, 1, 2, 2, 1, 2, 2, 2]
    k = 2
    result = solution.numberOfSubarrays(nums, k)
    print(result)
    assert result == 16
