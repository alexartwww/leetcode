# Условие задачи (на русском):
# Дан массив целых чисел 'nums' и число 'k'.
# За одну операцию можно выбрать два числа, сумма которых равна 'k', и удалить их из массива.
# Нужно вернуть МАКСИМАЛЬНОЕ количество таких операций.
#
# Пример 1: nums = [1, 2, 3, 4], k = 5
# 1. Берем 1 и 4 (1 + 4 = 5). Удаляем.
# 2. Берем 2 и 3 (2 + 3 = 5). Удаляем.
# Итог: 2 операции.
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Сортировка + Два указателя
#
# Идея: Если мы отсортируем массив, мы сможем эффективно искать пары, используя
# два указателя: один в начале (самое маленькое число), другой в конце (самое большое).
#
# 1. Сортировка: Сначала сортируем nums.
# 2. Указатели: left = 0, right = len(nums) - 1.
# 3. Сравнение:
#    - Если nums[left] + nums[right] == k:
#      Нашли пару! Увеличиваем счетчик операций и сдвигаем оба указателя внутрь.
#    - Если сумма < k:
#      Нам нужно число побольше. Сдвигаем левый указатель (left += 1).
#    - Если сумма > k:
#      Нам нужно число поменьше. Сдвигаем правый указатель (right -= 1).
#
# Пример 2 детально: nums = [3, 1, 3, 4, 3], k = 6
# 1. Сортируем: [1, 3, 3, 3, 4]
# 2. left=1, right=4. Сумма 1+4=5. 5 < 6, значит left += 1.
# 3. left=3, right=4. Сумма 3+4=7. 7 > 6, значит right -= 1.
# 4. left=3, right=3. Сумма 3+3=6. ПАРА! res = 1, left += 1, right -= 1.
# 5. Указатели встретились. Конец.
# Итог: 1

class Solution(object):
    def maxOperations(self, nums, k):
        # 1. Сортируем массив
        nums.sort()

        left = 0
        right = len(nums) - 1
        operations = 0

        # 2. Двигаем указатели навстречу друг другу
        while left < right:
            current_sum = nums[left] + nums[right]

            if current_sum == k:
                # Нашли пару
                operations += 1
                left += 1
                right -= 1
            elif current_sum < k:
                # Сумма слишком мала, нужно увеличить левое слагаемое
                left += 1
            else:
                # Сумма слишком велика, нужно уменьшить правое слагаемое
                right -= 1

        return operations


if __name__ == '__main__':
    solution = Solution()

    nums = [1, 2, 3, 4]
    k = 5
    result = solution.maxOperations(nums, k)
    print(result)
    assert result == 2

    nums = [3, 1, 3, 4, 3]
    k = 6
    result = solution.maxOperations(nums, k)
    print(result)
    assert result == 1
