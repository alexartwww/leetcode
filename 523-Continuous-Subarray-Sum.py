# leetcode: https://leetcode.com/problems/continuous-subarray-sum/description/
# Условие задачи:
# Дан массив целых чисел 'nums' и целое число 'k'. Нужно вернуть True, если в массиве
# есть "хороший" подмассив, иначе — False.
#
# "Хороший" подмассив — это:
# 1. Его длина как минимум два элемента (length >= 2).
# 2. Сумма его элементов делится на 'k' без остатка (кратно k).
# * 0 всегда считается кратным любому k.
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Префиксные суммы + Остатки от деления (Modulo)
#
# Математическая суть:
# Если сумма элементов от 0 до индекса i дает остаток R (при делении на k),
# и сумма элементов от 0 до индекса j дает тот же остаток R,
# значит сумма элементов МЕЖДУ i и j делится на k нацело.
#
# Формула: (Sum_j - Sum_i) % k == 0  =>  Sum_j % k == Sum_i % k
#
# 1. Инициализация:
#    - remainder_map = {0: -1}. Это критически важно!
#      Мы записываем, что остаток 0 мы "встретили" еще до начала массива (индекс -1).
#    - current_sum = 0 (накопительная сумма).
#
# 2. Проход по массиву:
#    - Добавляем текущее число к current_sum.
#    - Находим остаток от деления на k: remainder = current_sum % k.
#
# 3. Проверка остатка:
#    - Если такой remainder уже есть в словаре:
#      - Проверяем длину подмассива: текущий_индекс - старый_индекс.
#      - Если длина >= 2, возвращаем True.
#    - Если такого остатка нет в словаре:
#      - Сохраняем его и текущий индекс: remainder_map[remainder] = i.
#      (Важно: не обновляем индекс, если остаток уже есть, так как нам нужен
#       самый первый индекс для максимальной длины).
#
# Пример: nums = [23, 2, 4, 6, 7], k = 6
# - Индекс -1: остаток 0 (записали в map)
# - Индекс 0: 23 % 6 = 5. Map: {0:-1, 5:0}
# - Индекс 1: (23+2) % 6 = 25 % 6 = 1. Map: {0:-1, 5:0, 1:1}
# - Индекс 2: (25+4) % 6 = 29 % 6 = 5. Опа! Остаток 5 уже был на индексе 0.
#   Проверяем длину: 2 - 0 = 2. Условие (длина >= 2) выполнено.
#   Результат: True.

class Solution(object):
    def checkSubarraySum(self, nums, k):
        # Словарь: {остаток: индекс, где он впервые встретился}
        remainder_map = {0: -1}
        current_sum = 0

        for i in range(len(nums)):
            current_sum += nums[i]
            remainder = current_sum % k

            if remainder in remainder_map:
                # Проверяем, что длина подмассива хотя бы 2
                if i - remainder_map[remainder] >= 2:
                    return True
            else:
                # Сохраняем индекс только при первой встрече остатка
                remainder_map[remainder] = i

        return False


if __name__ == '__main__':
    solution = Solution()

    nums = [23, 2, 4, 6, 7]
    k = 6
    result = solution.checkSubarraySum(nums, k)
    print(result)
    assert result == True

    nums = [23, 2, 6, 4, 7]
    k = 6
    result = solution.checkSubarraySum(nums, k)
    print(result)
    assert result == True

    nums = [23, 2, 6, 4, 7]
    k = 13
    result = solution.checkSubarraySum(nums, k)
    print(result)
    assert result == 0
