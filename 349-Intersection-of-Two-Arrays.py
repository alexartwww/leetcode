# Условие задачи (на русском):
# Даны два массива 'nums1' и 'nums2'. Нужно вернуть их пересечение.
# Пересечение — это числа, которые есть в ОБОИХ массивах.
# Каждый элемент в ответе должен быть УНИКАЛЬНЫМ. Порядок не важен.
#
# Пример 1: nums1 = [1,2,2,1], nums2 = [2,2]
# Число 2 есть и там, и там. Хотя оно встречается дважды, в ответе пишем один раз.
# Результат: [2]
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Использование множеств (Set)
#
# Идея:
# 1. Множество в Python автоматически удаляет дубликаты.
# 2. Поиск в множестве (операция 'in') происходит за O(1), в отличие от списка O(n).
#
# Способ 1 (Самый быстрый в Python):
# Использовать встроенный оператор '&' (пересечение множеств).
#
# Способ 2 (Алгоритмический):
# 1. Превратить один массив в множество (set1).
# 2. Создать пустое множество для результата (res).
# 3. Пройтись по второму массиву: если число есть в set1, добавить его в res.
# 4. Вернуть res в виде списка.

class Solution(object):
    def intersection(self, nums1, nums2):
        set1 = set(nums1)
        res = set()

        for x in nums2:
            if x in set1:
                res.add(x)

        return list(res)


if __name__ == '__main__':
    solution = Solution()

    nums1 = [1, 2, 2, 1]
    nums2 = [2, 2]
    result = solution.intersection(nums1, nums2)
    print(result)
    assert result == [2]

    nums1 = [4, 9, 5]
    nums2 = [9, 4, 9, 8, 4]
    result = solution.intersection(nums1, nums2)
    print(result)
    assert result == [9, 4] or result == [4, 9]
