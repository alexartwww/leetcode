# leetcode: https://leetcode.com/problems/maximum-average-subarray-i/

# Эта задача — классический пример применения техники Sliding Window (Скользящее окно) фиксированного размера.
#
# Поскольку длина подмассива всегда равна k, нам не нужно каждый раз суммировать все k элементов заново. Достаточно
# один раз посчитать сумму первого окна, а затем «сдвигать» его, прибавляя новый элемент справа
# и вычитая ушедший элемент слева.

# Условие задачи (на русском):
# Дан массив 'nums' и число 'k'.
# Нужно найти непрерывный подмассив длиной ровно 'k',
# среднее арифметическое которого максимально.
#
# Пример 1: nums = [1, 12, -5, -6, 50, 3], k = 4
# Окно [1, 12, -5, -6] -> сумма 2
# Окно [12, -5, -6, 50] -> сумма 51 (максимальная)
# Окно [-5, -6, 50, 3] -> сумма 42
# Результат: 51 / 4 = 12.75
#
# -----------------------------------------------------------------------------
# Алгоритм решения: Скользящее окно (Sliding Window)
#
# Идея:
# 1. Вместо того чтобы искать среднее, будем искать максимальную СУММУ.
#    Разделим на 'k' только в самом конце — так мы избежим лишних делений.
# 2. Вычисляем сумму первых 'k' элементов.
# 3. Двигаем окно: вычитаем nums[i-k] и прибавляем nums[i].
#
# 1. Инициализация:
#    - current_sum = сумма первых k элементов.
#    - max_sum = current_sum.
#
# 2. Проход по массиву (от k до конца):
#    - current_sum += nums[i] - nums[i - k]
#    - Обновляем max_sum.
#
# 3. Итог: max_sum / k.

class Solution(object):
    def findMaxAverage(self, nums, k):
        # 1. Считаем сумму первого окна
        current_sum = sum(nums[:k])
        max_sum = current_sum

        # 2. Скользим по массиву
        for i in range(k, len(nums)):
            # Прибавляем элемент справа, убираем элемент слева
            current_sum += nums[i] - nums[i - k]

            # Обновляем максимум
            if current_sum > max_sum:
                max_sum = current_sum

        # 3. Возвращаем среднее значение
        return float(max_sum) / k


if __name__ == '__main__':
    solution = Solution()

    nums = [1, 12, -5, -6, 50, 3]
    k = 4
    result = solution.findMaxAverage(nums, k)
    print(result)
    assert result == 12.75000

    nums = [5]
    k = 1
    result = solution.findMaxAverage(nums, k)
    print(result)
    assert result == 5
